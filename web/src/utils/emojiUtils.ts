export function getAllEmojis(): Array<{
  emoji: string;
  name: string;
  category: string;
}> {
  return Object.entries(EMOJI_TO_CARTOON_MAP).map(([emoji, cartoonName]) => {
    const category = Object.keys(EMOJI_CATEGORIES).find((cat) =>
      EMOJI_CATEGORIES[cat].emojis.includes(emoji),
    );
    return { emoji, name: cartoonName, category: category || "objects" };
  });
}

